package com.project.smartfit.dto;

public class SaveTrainingUnitAddress {

    private String name;
    private String outerNumber;
    private String insideNumber;
    private String state;
    private String city;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getOuterNumber() {
        return outerNumber;
    }

    public void setOuterNumber(String outerNumber) {
        this.outerNumber = outerNumber;
    }

    public String getInsideNumber() {
        return insideNumber;
    }

    public void setInsideNumber(String insideNumber) {
        this.insideNumber = insideNumber;
    }

    public String getState() {
        return state;
    }

    public void setState(String state) {
        this.state = state;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }
}
